public class CommentsExample {
	/*
	main함수(메서드)는 프로그램을 실행하기 위해 반드시 작성해야 하는 함수(메서드)입니다.
	main함수 블록({})안에 있는 코드의 명령들을 실행합니다.
	main 함수를 쉽게 작성하려면 이클립스 코드작성부에 main이라고 입력하고
	ctrl + space를 누르면 자동 완성 기능으로 작성할 수 있다.
	*/
	public static void main(String[] args) {
		System.out.print("안녕~!\n");
		System.out.print("잘가~!\n");
		
		System.out.println("메롱~!");    // println은 \n을 포함한다.
		System.out.println("빠이~!");	//sysout >> 자동완성
		
		System.out.printf("%d+%d=%d\n",10,3,13);
	}
}
