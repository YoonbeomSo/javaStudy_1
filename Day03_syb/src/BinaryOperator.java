
public class BinaryOperator {

	public static void main(String[] args) {
		
		//산술연산자(+.-.*./.%)
		int i = 7/3;
		int j = 7%3;
		System.out.printf("몫 : %d \n나머지 : %d\n",i,j);
	
		//비교연산자(<, <=, >, >=, ==, !=)
		System.out.println(i==j);
		System.out.println(20>=20);
		System.out.println(130 < 50);
			
	}
}
